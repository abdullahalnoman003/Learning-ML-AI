# ---------------------------------------------------------
# WHAT IS print()?
# ---------------------------------------------------------
# print() is a built-in Python command (called a "function").
# Whatever you put inside the parentheses () gets displayed
# on the screen when you run the program.
# Think of it like telling Python: "Show this to me!"

print("Hello World! This is the first code written for learning Python.")

# You can print any text you want. Just wrap it in quotes.
print("My name is Abdullah Al Noman")
print("I am learning Python today!")

# ---------------------------------------------------------
# PRINTING NUMBERS
# ---------------------------------------------------------
# You can also print numbers without any quotes.

print(100)
print(3.14)

# ---------------------------------------------------------
# PRINTING MULTIPLE THINGS AT ONCE
# ---------------------------------------------------------
# Use a comma (,) to print multiple values on the same line.
# Python will automatically put a space between them.

print("My age is:", 22)
print("Pi is approximately:", 3.14159)

# ---------------------------------------------------------
# WHAT IS A COMMENT?
# ---------------------------------------------------------
# A comment starts with the # symbol.
# Python completely IGNORES comments when running the code.
# We use comments to write notes and explanations for ourselves.
# This is very helpful when you come back to read your code later!

# This line is a comment - Python ignores it.
print("Comments help us understand the code!")  # This is an inline comment

# ---------------------------------------------------------
# PRINTING A BLANK LINE
# ---------------------------------------------------------
# Calling print() with nothing inside it prints an empty line.
# Useful for spacing out your output.

print()  # This prints a blank/empty line
print("Python is awesome!")

# ============================================================
# SUMMARY:
# - print() displays output on the screen
# - Text (strings) must be inside quotes: "Hello"
# - Numbers do NOT need quotes: 42, 3.14
# - Comments start with # and are ignored by Python
